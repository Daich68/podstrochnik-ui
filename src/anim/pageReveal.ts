type Listener = () => void;

const listeners = new Set<Listener>();

// Момент, когда чернильная шторка (первая загрузка — Preloader,
// внутренняя навигация — RouteTransition) реально открывает страницу
// зрителю. Контент монтируется заранее (ради быстрой загрузки данных),
// но входные анимации должны стартовать не раньше этого сигнала —
// иначе они целиком проигрываются за закрытой шторкой и пропадают.
//
// Рассылка отложена макротаском (не микротаском!): вызов идёт синхронно
// изнутри чужого GSAP-эффекта/таймлайна, и setState в другом компоненте,
// сработавший той же микрозадачей/тем же тиком, обрывал таймлайн шторки
// на середине (проверено эмпирически — с queueMicrotask шторка виснет
// навсегда, с setTimeout доигрывает штатно). setTimeout выносит рассылку
// в отдельный макротаск, полностью вне текущего цикла коммита/эффектов.
export const notifyPageRevealed = () => {
    setTimeout(() => listeners.forEach(l => l()), 50);
};

export const onPageRevealed = (cb: Listener): (() => void) => {
    listeners.add(cb);
    return () => listeners.delete(cb);
};
